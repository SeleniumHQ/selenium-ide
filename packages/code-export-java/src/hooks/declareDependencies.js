let hooks = []

function register(statement) {
  hooks.push(statement)
}

function clear() {
  hooks = []
}

function emit({ isOptional } = { isOptional: false }) {
  if (isOptional && !hooks.length) return ''
  return dependencies + hooks.join('\r\n')
}

export default {
  register,
  clear,
  emit,
}
